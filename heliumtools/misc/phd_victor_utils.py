        self.boxes["1"]["Vz"]["size"] = dVz * ratio_size["Vz"]
        self.boxes["1"]["Vx"]["size"] = dVx * ratio_size["Vx"]
        self.boxes["1"]["Vy"]["size"] = dVy * ratio_size["Vy"]
        self.boxes["2"]["Vz"]["size"] = dVz
        self.boxes["2"]["Vx"]["size"] = dVx
